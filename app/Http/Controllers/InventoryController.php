    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'name' => 'required',
            'email' => 'required|email',
            'password' => 'required|min:4|max:25|same:password_confirmation',
            'password_confirmation'=>'same:password'
        ]);
        $inventory = new Inventory;
        $inventory->name = $request->name;
        $inventory->quantity = $request->quantity;
        $inventory->current_quantity = $request->current_quantity;
        $inventory->min_quantity = $request->min_quantity;
        $inventory->max_quantity = $request->max_quantity;
        $inventory->price = $request->price;
        $inventory->serial = $request->serial;
        $inventory->save();

        session()->flash('success', '300');
        return redirect()->route('livewire.inventory.index')->with('success', 'Created successfully!');
    }

